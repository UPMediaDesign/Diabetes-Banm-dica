					<script type="text/javascript">
						
						function changeStep(){
							
							step = jQuery('#startFlujo').attr('data-step');
							
							if(step == 1){
								jQuery('.program-flux__block').removeClass('active')
								jQuery('#flux-block-1').addClass('active')
								jQuery('#startFlujo').attr('data-step', parseInt(step)+1)
							}
							
							else if(step == 2){
								jQuery('.program-flux__block').removeClass('active')
								jQuery('#flux-block-2').addClass('active')
								jQuery('#startFlujo').attr('data-step', parseInt(step)+1)
							}
							
							else if(step == 3){
								jQuery('.program-flux__block').removeClass('active')
								jQuery('#flux-block-3').addClass('active')
								jQuery('#startFlujo').attr('data-step', 1)
							}
							
						}
						
						jQuery('.program-flux__block').hover(
						  function() {
							jQuery('.program-flux__block.active').removeClass('active');
							jQuery( this ).addClass('active');
							jQuery('#startFlujo').attr('data-step', 1)
						  }, function() {
							jQuery( this ).removeClass('active');
						  }
						);
						
					</script>
